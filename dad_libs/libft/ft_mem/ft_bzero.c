#include "../include/libft.h"

void	ft_bzero(void *s, size_t n)
{
	char	*c_s;
	size_t	i;

	if (!n)
		return ;
	c_s = (char*)s;
	i = -1;
	while (++i < n)
		c_s[i] = 0;
}
