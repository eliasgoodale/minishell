#include "../include/libft.h"

int		ft_max(int *tab, size_t len)
{
	size_t	i;
	int		max;

	i = 0;
	max = tab[i];
	while (i < len)
	{
		if (tab[i] > max)
			max = tab[i];
		i++;
	}
	return (max);
}
